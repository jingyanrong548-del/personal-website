let currentLanguage = 'en';

export function getCurrentLanguage() {
    return currentLanguage;
}

export function detectInitialLanguage() {
    return localStorage.getItem('language') || 'en';
}

function updateMetaForLanguage(lang) {
    const seoTitle = translations[lang]?.['seo.pageTitle'];
    const seoDesc = translations[lang]?.['seo.pageDescription'];
    if (seoTitle) document.title = seoTitle;
    document.getElementById('meta-page-description')?.setAttribute('content', seoDesc || '');
    document.getElementById('meta-page-title')?.setAttribute('content', seoTitle || '');
    document.getElementById('meta-og-title')?.setAttribute('content', seoTitle || '');
    document.getElementById('meta-og-description')?.setAttribute('content', seoDesc || '');
    document.getElementById('meta-twitter-title')?.setAttribute('content', seoTitle || '');
    document.getElementById('meta-twitter-description')?.setAttribute('content', seoDesc || '');

    const ogLoc = document.querySelector('meta[property="og:locale"]');
    const ogLocAlt = document.querySelector('meta[property="og:locale:alternate"]');
    if (ogLoc && ogLocAlt) {
        if (lang === 'zh') {
            ogLoc.setAttribute('content', 'zh_CN');
            ogLocAlt.setAttribute('content', 'en_US');
        } else {
            ogLoc.setAttribute('content', 'en_US');
            ogLocAlt.setAttribute('content', 'zh_CN');
        }
    }
}

function updateLanguageButtons(lang) {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
}

function updateVCardForLanguage(lang) {
    const vcardDownloadBtn = document.getElementById('vcard-download-btn');
    if (vcardDownloadBtn) {
        if (lang === 'zh') {
            vcardDownloadBtn.href = '/jingyanrong-zh.vcf';
            vcardDownloadBtn.download = 'jingyanrong-zh.vcf';
        } else {
            vcardDownloadBtn.href = '/jingyanrong-en.vcf';
            vcardDownloadBtn.download = 'jingyanrong-en.vcf';
        }
    }

    const vcardQRCodeImg = document.getElementById('vcard-qrcode');
    if (vcardQRCodeImg) {
        const baseURL = window.location.origin;
        const vcardURL = lang === 'zh' ? `${baseURL}/jingyanrong-zh.vcf` : `${baseURL}/jingyanrong-en.vcf`;
        vcardQRCodeImg.src = '';
        const qrCodeURL = `https://api.qrserver.com/v1/create-qr-code/?size=280x280&data=${encodeURIComponent(vcardURL)}&t=${Date.now()}`;
        vcardQRCodeImg.src = qrCodeURL;
    }
}

export function setLanguage(lang, options = {}) {
    if (!translations[lang]) return;

    currentLanguage = lang;
    localStorage.setItem('language', lang);

    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;

    updateMetaForLanguage(lang);

    document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
        const key = el.getAttribute('data-i18n-aria-label');
        if (key && translations[lang][key]) {
            el.setAttribute('aria-label', translations[lang][key]);
        }
    });
    document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
        const key = el.getAttribute('data-i18n-alt');
        if (key && translations[lang][key]) {
            el.setAttribute('alt', translations[lang][key]);
        }
    });

    const currentYear = new Date().getFullYear();
    const currentDate = new Date();
    const appCount = document.querySelectorAll('#apps .app-card').length;

    const startYear = 1998;
    const startMonth = 6; // July (0-indexed)
    const startDay = 1;
    let experienceYears = currentYear - startYear;
    const currentMonth = currentDate.getMonth();
    const currentDay = currentDate.getDate();
    if (currentMonth < startMonth || (currentMonth === startMonth && currentDay < startDay)) {
        experienceYears--;
    }

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (!key || !translations[lang] || !translations[lang][key]) return;

        let text = translations[lang][key];

        if (key.startsWith('apps.app') && key.endsWith('.title')) {
            const appMatch = key.match(/apps\.app(.+?)\.title$/);
            if (appMatch) {
                const appKey = appMatch[1];
                const versionKey = `apps.app${appKey}.version`;
                const version = translations[lang][versionKey];
                if (version && version.trim() !== '') {
                    text = `${text} ${version}`;
                }
            }
        }

        text = text.replace(/{year}/g, currentYear.toString());
        text = text.replace(/{count}/g, appCount.toString());
        text = text.replace(/{experienceYears}/g, experienceYears.toString());

        try {
            const allowHtml = element.getAttribute('data-i18n-html') === 'true';
            if (allowHtml) {
                element.innerHTML = text;
            } else {
                element.textContent = text;
            }
            if (!text || text.trim() === '') {
                element.classList.add('u-hidden');
            } else {
                element.classList.remove('u-hidden');
            }
        } catch (error) {
            console.warn('Failed to update element with key:', key, error);
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (key && translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    updateLanguageButtons(lang);
    updateVCardForLanguage(lang);

    if (typeof options.afterSet === 'function') {
        options.afterSet(lang);
    }
}

export function initLanguageSwitcher(options = {}) {
    const initial = detectInitialLanguage();
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            setLanguage(lang, options);
        });
    });
    setLanguage(initial, options);
}

